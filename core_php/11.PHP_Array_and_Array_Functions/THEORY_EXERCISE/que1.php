Define arrays in PHP. What are the different types of arrays?

ans:

In PHP, an array is a data structure that allows you to store multiple values in a single variable. Arrays can hold elements of any data type (integers, strings, objects, other arrays, etc.), and these elements are accessed using keys or indices. Arrays in PHP are flexible and can store values in sequential or associative formats.

Types of Arrays in PHP
There are three main types of arrays in PHP:

1.Indexed Arrays (Numeric Arrays)
2.Associative Arrays
3.Multidimensional Arrays
